/**
 * 決定要回給呼叫端的錯誤訊息，依可靠程度排序：
 *   1. 解密後的 Message（最精確，會帶實際情境，例如「處理失敗，查無符合訂單」）
 *   2. 外層 Message
 *   3. 錯誤碼對照表
 *   4. 直接顯示錯誤碼（至少讓人查得到）
 */
function payuni_resolve_error_message($code, $decryptedMessage = null, $outerMessage = null, $fallback = '請求失敗') {
    if (!empty($decryptedMessage)) {
        return $decryptedMessage;
    }
    if (!empty($outerMessage)) {
        return $outerMessage;
    }
    $mapped = payuni_error_message($code);
    if ($mapped !== null) {
        return $mapped . '（' . $code . '）';
    }
    return !empty($code) ? ($fallback . '（' . $code . '）') : $fallback;
}
